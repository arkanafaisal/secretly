const getInitialLanguage = () => {
  const storedLang = localStorage.getItem('app_lang');
  if (storedLang === 'eng' || storedLang === 'id') return storedLang;
  
  const browserLang = navigator.language || navigator.userLanguage;
  return browserLang.toLowerCase().startsWith('id') ? 'id' : 'eng';
};

export default function useTranslation() {
  const [lang, setLang] = useState(getInitialLanguage);

  useEffect(() => {
    const handleLanguageChange = (e) => {
      setLang(e.detail);
    };

    window.addEventListener('app_lang_change', handleLanguageChange);
    return () => window.removeEventListener('app_lang_change', handleLanguageChange);
  }, []);

  const changeLanguage = (newLang) => {
    localStorage.setItem('app_lang', newLang);
    window.dispatchEvent(new CustomEvent('app_lang_change', { detail: newLang }));
  };

  const t = (key) => translations[lang]?.[key] || translations['eng'][key] || key;

  return { t, lang, changeLanguage };
}